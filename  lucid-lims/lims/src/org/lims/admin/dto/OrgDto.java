package org.lims.admin.dto;

/**
 * @author Muralidhar Yaragalla
 *
 */
public class OrgDto {
	
	private String orgName;
	private String orgAddress;
	private String orgPhone;
	private String orgFax;
	private String orgEmail;
	private String orgWebsite;
	
	
	/**
	 * @return the orgName
	 */
	public String getOrgName() {
		return orgName;
	}
	/**
	 * @param orgName the orgName to set
	 */
	public void setOrgName(String orgName) {
		this.orgName = orgName;
	}
	/**
	 * @return the orgAddress
	 */
	public String getOrgAddress() {
		return orgAddress;
	}
	/**
	 * @param orgAddress the orgAddress to set
	 */
	public void setOrgAddress(String orgAddress) {
		this.orgAddress = orgAddress;
	}
	/**
	 * @return the orgPhone
	 */
	public String getOrgPhone() {
		return orgPhone;
	}
	/**
	 * @param orgPhone the orgPhone to set
	 */
	public void setOrgPhone(String orgPhone) {
		this.orgPhone = orgPhone;
	}
	/**
	 * @return the orgFax
	 */
	public String getOrgFax() {
		return orgFax;
	}
	/**
	 * @param orgFax the orgFax to set
	 */
	public void setOrgFax(String orgFax) {
		this.orgFax = orgFax;
	}
	/**
	 * @return the orgEmail
	 */
	public String getOrgEmail() {
		return orgEmail;
	}
	/**
	 * @param orgEmail the orgEmail to set
	 */
	public void setOrgEmail(String orgEmail) {
		this.orgEmail = orgEmail;
	}
	/**
	 * @return the orgWebsite
	 */
	public String getOrgWebsite() {
		return orgWebsite;
	}
	/**
	 * @param orgWebsite the orgWebsite to set
	 */
	public void setOrgWebsite(String orgWebsite) {
		this.orgWebsite = orgWebsite;
	}

}
