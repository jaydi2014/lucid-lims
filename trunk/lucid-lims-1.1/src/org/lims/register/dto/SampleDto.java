package org.lims.register.dto;

/**
 * @author Muralidhar Yaragalla
 *
 */
public class SampleDto {

	private int sampleId;
	private String sampleName;
	private String sampleTests;
	private String sampleQty;
	private String batchMfgDetails;
	private String specification;
	private String testMethod;
	private String serialNo;
	
	
	/**
	 * @return the sampleId
	 */
	public int getSampleId() {
		return sampleId;
	}
	/**
	 * @param sampleId the sampleId to set
	 */
	public void setSampleId(int sampleId) {
		this.sampleId = sampleId;
	}
	/**
	 * @return the sampleName
	 */
	public String getSampleName() {
		return sampleName;
	}
	/**
	 * @param sampleName the sampleName to set
	 */
	public void setSampleName(String sampleName) {
		this.sampleName = sampleName;
	}
	/**
	 * @return the sampleTests
	 */
	public String getSampleTests() {
		return sampleTests;
	}
	/**
	 * @param sampleTests the sampleTests to set
	 */
	public void setSampleTests(String sampleTests) {
		this.sampleTests = sampleTests;
	}
	/**
	 * @return the sampleQty
	 */
	public String getSampleQty() {
		return sampleQty;
	}
	/**
	 * @param sampleQty the sampleQty to set
	 */
	public void setSampleQty(String sampleQty) {
		this.sampleQty = sampleQty;
	}
	/**
	 * @return the batchMfgDetails
	 */
	public String getBatchMfgDetails() {
		return batchMfgDetails;
	}
	/**
	 * @param batchMfgDetails the batchMfgDetails to set
	 */
	public void setBatchMfgDetails(String batchMfgDetails) {
		this.batchMfgDetails = batchMfgDetails;
	}
	/**
	 * @return the specification
	 */
	public String getSpecification() {
		return specification;
	}
	/**
	 * @param specification the specification to set
	 */
	public void setSpecification(String specification) {
		this.specification = specification;
	}
	/**
	 * @return the testMethod
	 */
	public String getTestMethod() {
		return testMethod;
	}
	/**
	 * @param testMethod the testMethod to set
	 */
	public void setTestMethod(String testMethod) {
		this.testMethod = testMethod;
	}
	/**
	 * @return the serialNo
	 */
	public String getSerialNo() {
		return serialNo;
	}
	/**
	 * @param serialNo the serialNo to set
	 */
	public void setSerialNo(String serialNo) {
		this.serialNo = serialNo;
	}
}
