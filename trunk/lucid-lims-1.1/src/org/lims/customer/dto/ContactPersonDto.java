public class ContactPersonDto {
	private Integer ctPersonId;
	private String ctPersonName;
	private String ctPersonMobile;
	private String ctPersonEmail;
	/**
	 * @return the ctPersonId
	 */
	public Integer getCtPersonId() {
		return ctPersonId;
	}
	/**
	 * @param ctPersonId the ctPersonId to set
	 */
	public void setCtPersonId(Integer ctPersonId) {
		this.ctPersonId = ctPersonId;
	}
	/**
	 * @return the ctPersonName
	 */
	public String getCtPersonName() {
		return ctPersonName;
	}
	/**
	 * @param ctPersonName the ctPersonName to set
	 */
	public void setCtPersonName(String ctPersonName) {
		this.ctPersonName = ctPersonName;
	}
	/**
	 * @return the ctPersonMobile
	 */
	public String getCtPersonMobile() {
		return ctPersonMobile;
	}
	/**
	 * @param ctPersonMobile the ctPersonMobile to set
	 */
	public void setCtPersonMobile(String ctPersonMobile) {
		this.ctPersonMobile = ctPersonMobile;
	}
	/**
	 * @return the ctPersonEmail
	 */
	public String getCtPersonEmail() {
		return ctPersonEmail;
	}
	/**
	 * @param ctPersonEmail the ctPersonEmail to set
	 */
	public void setCtPersonEmail(String ctPersonEmail) {
		this.ctPersonEmail = ctPersonEmail;
	}
	
	
}
